    private void rellenarArrays(int orden, int contenido) throws SQLException{
        this.contenido = contenido;
        this.orden = orden;
        String sentencia = "";
        if(contenido != 0){
            sentencia = " WHERE ";
        }
        String [] arrayOrden = {"clase", "titulo", "año DESC", "valoracion DESC"};
        String [] arrayContenido = {"", "clase = 'Anime'", "clase = 'Serie'", "clase = 'Película [Anime]'", 
        "clase = 'Película'", "clase = 'PS4'", "clase = 'PC'"};
        rs = controller.EjecutarSentencia("SELECT COUNT(*) FROM MULTILISTA" + sentencia + arrayContenido[contenido]);
        rs.next();
        String [] array1 = new String[rs.getInt(1)];
        if(contenido != 0){
            sentencia = " AND ";
        }
        rs = controller.EjecutarSentencia("SELECT COUNT(*) FROM MULTILISTA WHERE usado = 0" + sentencia + arrayContenido[contenido]);
        rs.next();
        String [] array2 = new String[rs.getInt(1)];
        if(contenido != 0){
            sentencia = " WHERE ";
        }
        rs = controller.EjecutarSentencia("SELECT * FROM MULTILISTA" + sentencia + arrayContenido[contenido] + " ORDER BY " + arrayOrden[orden]);
        rs.next();
        for (int i = 0; i < array1.length; i++) {
            array1[i] = (new Multimedia(rs.getString(5), rs.getString(1), rs.getInt(2), rs.getString(3), rs.getInt(4), rs.getString(6))).toString();
            rs.next();
        }
        if(contenido != 0){
            sentencia = " AND ";
        }
        rs = controller.EjecutarSentencia("SELECT * FROM MULTILISTA WHERE usado = 0" + sentencia + arrayContenido[contenido] + " ORDER BY " + arrayOrden[orden]);
        rs.next();
        for (int i = 0; i < array2.length; i++) {
            array2[i] = (new Multimedia(rs.getString(5), rs.getString(1), rs.getInt(2), rs.getString(3), rs.getInt(4), rs.getString(6))).toString();
            rs.next();
        }
        jList1.setListData(array1);
        jList2.setListData(array2);
    }
    
