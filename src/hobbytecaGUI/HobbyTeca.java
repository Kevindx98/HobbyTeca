    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
        if(jList1.getSelectedValue() != null){
            String s = "SELECT * FROM MULTILISTA WHERE titulo = '" + jList1.getSelectedValue().substring(jList1.getSelectedValue().indexOf("] ") + 2, jList1.getSelectedValue().indexOf(" (")) + "'";
            try {
                rs = controller.EjecutarSentencia(s);
                rs.next();
                int indice = 0;
                switch (rs.getString(5)) {
                    case "PS4":
                        indice = 1;
                        break;
                    case "Película":
                        indice = 2;
                        break;
                    case "Película [Anime]":
                        indice = 3;
                        break;
                    case "Serie":
                        indice = 4;
                        break;
                    case "Anime":
                        indice = 5;
                        break;
                    default:
                        break;
                }
                NuevoContenido nuevoContenido = new NuevoContenido(this, true, "Modificar Contenido", rs.getString(1), rs.getInt(2), rs.getString(3), rs.getInt(4), indice, rs.getString(6));
                nuevoContenido.setVisible(true);
                rellenarArrays(orden, contenido);
            } catch (SQLException ex) {
                Logger.getLogger(HobbyTeca.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jButton5ActionPerformed

